    <div class="message-body">
      为了使5级车辆成为可能，车辆需要具有像人类驾驶员一样的“思考”、“感知”和“反应”的能力。由于人工智能在不同领域特别是在图像分类、目标检测和语音识别方面取得了许多最新成就，人工智能技术（如深度学习（DL）和强化学习（RL））越来越多地用于去实现5级车辆。基于DL的方法，许多研究能够解决AVS中各种挑战性问题，例如准确识别和定位道路上的障碍物、合适的车辆控制和运动规划。
      <br/>
      <br/>
      本文试图综述最近用于处理AVS中主要功能的基于人工智能的技术，即场景理解、运动规划、决策、车辆控制、社会行为和通信。我们的研究仅关注基于DL和RL的方法，因此忽略了基于浅层机器学习（ML）的技术，因为这个主题在过去已经被广泛研究（Qayyum et al., 2019; Elassad et al., 2020）。传统的ML方法在实现AVS的主要功能（例如场景理解和运动规划）方面表现出了局限性。这促使我们将研究限制在DL和RL方法，以显示其在实现这些功能方面的潜力。
      <br/>
      <br/>
      DL和RL技术以其改善AVS任务的能力而闻名。例如，DL在目标检测方面表现出了良好的效果，这使其适合于自动驾驶中的场景理解。RL已成功应用于游戏和机器人等需要从环境中学习的其他领域。近年来，由于其取得了许多富有前景的结果，RL技术越来越受到AVS研究界的关注。
      <br/>
      <br/>
      本文回顾安排如下：第一节讨论了现有的研究，并强调了我们想用当前研究填补的空白，同时介绍了我们的研究范围和研究方法。第二节简要介绍了应用于AVS的DL和RL方法。第三节介绍了AVS的不同传感器。第四节介绍了最近应用于AVS的基于DL的方法。第五节介绍了现有的解决自动驾驶问题的基于RL的方法。第六节提出了有待解决的研究问题和未来的研究方向。最后，第七节总结全文。
