      <b>1.2 现有综述的介绍</b>
      <br/>
      <br/>
      Ohn-Bar and Trivedi (2016)讨论了下一代智能车辆中人类行为的理解、建模和预测，例如人与自动车辆内部或周围车辆之间的交互。该文献研究了自主车辆技术在各种方面遇到的问题。但是这些研究大多没有详细讨论人工智能技术的使用，而且关于DL和RL在不同应用中的使用的讨论更是寥寥无几。
      <br/>
      <br/>
      Xue et al. (2018) 研究了自动驾驶车辆中交通场景理解的功能，以及该功能如何帮助事件检测和意图预测。Elassad et al. (2020) 比较了机器学习（ML）模型和非ML模型之间的估计精度，确定了ML技术用于评估驾驶行为的优缺点。Qayyum et al. (2019) 讨论了与ML在车辆网络中的应用相关的挑战，并强调了由于采用ML方法而产生的不同安全问题。
      <br/>
      <br/>
      Hussain and Zeadally (2018)全面回顾了自动驾驶汽车技术，并描述了自动驾驶汽车的设计和实施问题，例如自动驾驶汽车行业面临的技术和非技术挑战。此外，他们还介绍了自主车辆技术优化方向的ML和DL的最新发展。然而，这项工作仅仅涉及到少数DL技术的几个方面，即感知、通信和控制，并且完全忽略了RL技术。
      <br/>
      <br/>
      Kuutti et al. (2020) 讨论了控制AVS的DL方法及其在复杂场景中的良好性能。作者介绍了应用于自主车辆控制的现有DL方法的优点和局限性。但是他的讨论没有涵盖自动驾驶的其他主要方面。
      <br/>
      <br/>
      Rasouli and Tsotsos (2019)根据行人的人口统计、交通动态和环境条件，审查了不同的行人行为。该研究主要内容是分析十字路口行人行为的不同方法。然而，作者没有介绍DL和RL技术来改善行人和AVS之间的通信。
      <br/>
      <br/>
      Ma et al. (2020) 分析了当前使用人工智能技术进行自动语音识别的实践，并讨论了与实现相关的挑战和问题。这篇文献的问题是他们并没有关注基于DL和RL的方法。
      <br/>
      <br/>
      Claussmann et al. (2019) 回顾了运动规划技术；他们的重点是公路规划。他们讨论了运动规划中的主要算法及其在公路驾驶中的应用。但是他们没有具体展示DL和RL在AVS领域中的作用。
      <br/>
      <br/>
      Grigorescu et al. (2020) 研究了自动驾驶中使用的DL技术，并强调了该技术在场景感知、路径规划、行为仲裁和运动控制方面的优势和局限性。他们强调了当前有关自动驾驶人工智能架构设计的主要挑战。
      <br/>
      <br/>
      Tong et al. (2019) 讨论了用于Vehicle toEverything（V2X）应用的人工智能方法，以及这些方法相对于传统算法的性能；他们也涵盖了人工智能在从不同来源获取信息方面的作用。这篇文献的缺点在于他们只关注车对车（V2V）和V2X中的信息共享问题。
      <br/>
      <br/>
      Deb et al. (2018) 回顾了行人与全自动汽车的相互作用；他们强调了行人的新行为，这可能会导致潜在风险，需要在自动驾驶汽车上路之前加以识别。然而，他们没有研究DL和RL方法在实时准确检测行人意图方面的作用。
      <br/>
      <br/>
      Feng et al. (2020) 提出了应用于自动驾驶的目标检测和分割系统。他们提出了与这些检测方法相关的挑战和开放性问题。
      <br/>
      <br/>
      Arnold et al. (2019) 研究了3D物体检测方法，以及AVS中常用的传感器和数据集。另外作者讨论了基于传感器模式的最新贡献，并将其分为三大类：单目、基于点云和融合方法。缺点是本次研究不包括DL方法，所以在广度上并不令人满意。
      <br/>
      <br/>
      Ning et al. (2021) 介绍了用于自动驾驶的现有人工智能架构。他们总结了这些架构的局限性，并引入了人工智能（H-AI）的概念；H-AI被认为是未来自主驾驶发展的一个新视角。他们还提出了未来需要解决的开放性研究挑战。然而，他们没有涵盖基于人工智能的解决方案，这些解决方案可能会在未来改善人工智能。
      <br/>
      <br/>
      Severino et al. (2021) 讨论了道路标线、十字路口和路面对AVS操作的影响，尤其是在驾驶时检测环境。他们还提出了实施AVS系统所引起的一些技术问题，比如说由于AVS整体技术还未完全成熟所带来的可靠性、稳定性、成本方面的问题。
      <br/>
      <br/>
      我们得出结论，现有研究没有全面涵盖DL和RL技术的使用，以解决AVS中与场景理解、运动规划、决策、车辆控制、社会行为和通信相关的主要问题。这促使我们通过介绍一项新的研究来填补文献中的这一空白，该研究致力于迄今为止应用于自主驾驶领域的大量DL和RL技术。表1总结了现有研究的覆盖范围。当前的研究揭示了DL和RL方法在自动驾驶汽车不同方面的潜力，并展示了在该领域可以实现的目标。与现有的自主驾驶研究不同，本文首次将重点放在DL和RL技术上，以实现AVS中的主要功能。
    </div>
