    <div id="背景"  class="message-body">
      <b>1.1 背景</b>
      <br/>
      <br/>
      道路伤害是世界上死亡的主要原因之一。世界卫生组织（WHO）的最新报告显示，每年约有130万人死于道路事故(Be, 2021)。这个的数字中有一半以上是弱势道路使用者(VRUs)，即行人、自行车和摩托车。其他研究表明，在所有VRU受害者中，行人仍站其中大多数(El Hamdani et al., 2020)。自动驾驶和辅助驾驶人工智能焦点小组（FG-AI4AD）最近的一项研究强调，道路伤害是儿童死亡的主要原因，这个原因造成的死亡数远远大于艾滋病毒和结核病引起的死亡。
      <br/>
      <br/>
      为了减少道路伤害，车辆网络（VANet）和自动驾驶汽车（AVS）越来越受到学术界和工业界的关注。人们认为这些技术将有效减少道路伤害造成的伤亡，并解决各种长期存在的交通挑战，即道路拥堵、行程延迟、停车和安全。在Singh（2015）中，所有车祸中有90%估计是人为失误造成的。智能交通系统（ITS）、计算系统和人工智能（AI）的最新进展为AVS的广泛引入铺平了道路。这为智能道路、智能交通安全和旅行者舒适度三个领域带来了新的机遇。研究人员估计，到2025年，将有800万辆自动驾驶汽车上路(Bay, 2021)。然而，由于过去发生的一些事件，尽管研究人员和汽车制造商提出不同方法去研究解决相关问题和剩余挑战（Hussain and Zeadally, 2018），AVS的普及仍然受到各种各样的阻碍。
      <br/>
      <br/>
      AVS能够处理复杂的道路情况，显著提高用户乘坐的安全性、舒适性和便利性的原因有以下几点：Autonomous Vehicle通常与self-driving vehicle互换使用，其能够感知其环境并在没有人类参与的情况下做出决策。自动驾驶汽车与道路旁基础设施和VRU相互合作收集和共享信息（Lamssaggad et al.2021）。AVS依靠车辆通信功能，在发生交通堵塞或事故时交换安全信息、交通状况和警告信息。无人驾驶车辆主要依靠传感器、执行器、复杂算法、人工智能技术和强大的计算资源来运行软件。
      <br/>
      <br/>
      美国汽车工程师学会（SAE）定义了6个级别的驾驶自动化，从全手动到全自动。0级和1级的特点是没有驾驶辅助，2级的定义是部分驾驶辅助，3级车辆具有“环境检测”能力，在没有人为干预的情况下，可以在低频率的情况下做出明智的决定，例如加速通过缓慢移动的车辆，而在4级（高驾驶自动化）中，驾驶员可以选择在系统故障时手动控制，最后，第5级是全自动驾驶，在任何情况下都不需要驾驶员（Ma et al., 2020）。
